package myutil;

public class MyDate3 {
	int year;
	int month;
	int day;
	//변수이름이 같을경우 어디꺼인지 정확히 명시를해야한다.
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	
	
}
